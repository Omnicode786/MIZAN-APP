function preserveOuterWhitespace(original: string, translated: string) {
  const prefix = original.match(/^\s*/)?.[0] || "";
  const suffix = original.match(/\s*$/)?.[0] || "";
  return `${prefix}${translated}${suffix}`;
}

function wordFallback(text: string, language: AppLanguage) {
  if (language !== "ur") return text;

  return text.replace(/[A-Za-z][A-Za-z'-]*/g, (word) => {
    const translated = urWords[word.toLowerCase()];
    return translated || word;
  });
}

function translatePart(part: string, dictionary: Record<string, string>, language: AppLanguage) {
  const trimmed = part.trim();
  if (!trimmed) return part;

  const translated = dictionary[trimmed];
  if (translated) return preserveOuterWhitespace(part, translated);

  return preserveOuterWhitespace(part, wordFallback(trimmed, language));
}

export function translateUiText(input: string, languageInput: unknown) {
  const language = normalizeLanguage(languageInput);
  if (language === "en") return input;

  const dictionary = language === "ur" ? urPhrases : romanPhrases;
  const trimmed = input.trim();
  if (!trimmed) return input;

  const exact = dictionary[trimmed];
  if (exact) return preserveOuterWhitespace(input, exact);

  if (trimmed.includes(" · ")) {
    return preserveOuterWhitespace(
      input,
      trimmed
        .split(" · ")
        .map((part) => translatePart(part, dictionary, language))
        .join(" · ")
    );
  }

  if (trimmed.includes(": ")) {
    const [first, ...rest] = trimmed.split(": ");
    return preserveOuterWhitespace(
      input,
      `${translatePart(first, dictionary, language)}: ${rest.join(": ")}`
    );
  }

  return preserveOuterWhitespace(input, wordFallback(trimmed, language));
}
